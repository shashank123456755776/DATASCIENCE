# UNDERSTANDING PYPLOT AND ITS FUNCTIONALITY 
# DATAPOINTS--The values that we want to plot on the graph
# x-axis/y-axis--The horizontal and vertical Lines that define teh graph
# figure--The entire window or page that hods the graph or plot 
# Plot--The function used to draw the graph or plot on the figure means help to plot line graph.
# Axes--The area on which the data is plotted,including the x-axis and y-axis
# Marker--The symbol used to represent each data point on the plot 
# Line--The line that connects then data points on the plot 
# Line Stye--The style of the line,such as solid,dashed,or dotted 
# Color--The color of the line and markers on the plot
# Legend--Use on outside the plot to explain the meaning of diffrent lines Or markers
# Title--The heading or the title of the plot or graph 
# Labels--the x-axis and y-axis names shows what data is being represented 
# Grid--The lines that run across the plot to help visualize the data points 
# Function --A block of code that performs a specific task 
# Method--A fucntion that is associated with an object
# Object--An instance of a class that contains data and methods to manipilate taht data
# Parameters--The values that are passed to a function  or method to customize its behaviour
# Keywords Arguments --Parameters that are used to specify the values of specific arguments in a function or method call  example kwargs (name=value)
# Object-Oriented API--Bhout sare graph hote hai figure mai then hame choose karna hota hai ki hame konsa graph ke under  kya plot karna hai
# DPI(DATA PER INCH)--The resolution of the figure or plot,measured in dots  per inch 
# Backend--The software component that handles the rendering of the plot or graph on teh screen  or in a file

